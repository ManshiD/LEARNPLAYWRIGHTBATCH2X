let age = "Pramod"; // This is a string literal assigned to the variable 'age'
let isStudent = true; // This is a boolean literal assigned to the variable 'isStudent'
let pi = 3.14; // This is a numeric literal assigned to the variable 'pi'
let name = 'John'; // This is a string literal assigned to the variable 'name'
let nullValue = null; // This is a null literal assigned to the variable 'nullValue'
let undefinedValue; // This variable is declared but not assigned, so it is undefined

//typeof operator can be used to check the type of a literal
console.log(typeof age); // Output: string 
console.log(typeof pi); // Output: number
console.log(typeof isStudent); // Output: boolean
console.log(typeof nullValue); // Output: object
console.log(typeof undefinedValue); // Output: undefined